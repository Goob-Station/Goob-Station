using Content.Client.UserInterface.Controls;
using Content.Goobstation.Shared.StockMarket;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Goobstation.Client.StockMarket;

[GenerateTypedNameReferences]
public sealed partial class StockMarketConsoleWindow : FancyWindow
{
    public event Action<string, int>? OnBuyPressed;
    public event Action<string, int>? OnSellPressed;

    private readonly Dictionary<string, StockRow> _rows = new();

    public StockMarketConsoleWindow()
    {
        RobustXamlLoader.Load(this);
    }

    public void UpdateState(StockMarketConsoleState state)
    {
        AccountLabel.Text = Loc.GetString("stock-market-account-label",
            ("account", state.ActiveAccount));
        BalanceLabel.Text = Loc.GetString("stock-market-balance-label",
            ("balance", state.AccountBalance));

        // Remove rows for stocks that no longer exist
        var toRemove = new List<string>();
        foreach (var (id, row) in _rows)
        {
            if (!state.Stocks.ContainsKey(id))
            {
                StockList.RemoveChild(row);
                toRemove.Add(id);
            }
        }
        foreach (var id in toRemove)
            _rows.Remove(id);

        // Update or create rows
        foreach (var (stockId, data) in state.Stocks)
        {
            if (!_rows.TryGetValue(stockId, out var row))
            {
                row = new StockRow(stockId);
                row.BuyButton.OnPressed += _ => OnBuyPressed?.Invoke(stockId, GetBuyAmount(row));
                row.SellButton.OnPressed += _ => OnSellPressed?.Invoke(stockId, GetSellAmount(row));
                _rows[stockId] = row;
                StockList.AddChild(row);
            }

            row.Update(data);
        }
    }

    private int GetBuyAmount(StockRow row)
    {
        return int.TryParse(row.AmountEdit.Text, out var amount) && amount > 0 ? amount : 1;
    }

    private int GetSellAmount(StockRow row)
    {
        return int.TryParse(row.AmountEdit.Text, out var amount) && amount > 0 ? amount : 1;
    }
}

/// <summary>
/// A single row in the stock market table.
/// </summary>
public sealed class StockRow : BoxContainer
{
    public readonly Label NameLabel;
    public readonly Label CategoryLabel;
    public readonly Label PriceLabel;
    public readonly Label ChangeLabel;
    public readonly Label OwnedLabel;
    public readonly LineEdit AmountEdit;
    public readonly Button BuyButton;
    public readonly Button SellButton;

    public string StockId { get; }

    public StockRow(string stockId)
    {
        StockId = stockId;
        Orientation = LayoutOrientation.Horizontal;
        Margin = new Thickness(5, 2, 5, 2);

        NameLabel = new Label { MinWidth = 140 };
        CategoryLabel = new Label { MinWidth = 100 };
        PriceLabel = new Label { MinWidth = 80, HorizontalAlignment = HAlignment.Right };
        ChangeLabel = new Label { MinWidth = 70, HorizontalAlignment = HAlignment.Right };
        OwnedLabel = new Label { MinWidth = 60, HorizontalAlignment = HAlignment.Right };

        AmountEdit = new LineEdit
        {
            MinWidth = 50,
            PlaceHolder = "1",
            HorizontalExpand = true,
        };

        BuyButton = new Button
        {
            Text = Loc.GetString("stock-market-buy-button"),
            MinWidth = 45,
        };

        SellButton = new Button
        {
            Text = Loc.GetString("stock-market-sell-button"),
            MinWidth = 45,
        };

        AddChild(NameLabel);
        AddChild(CategoryLabel);
        AddChild(PriceLabel);
        AddChild(ChangeLabel);
        AddChild(OwnedLabel);
        AddChild(new Control { HorizontalExpand = true });
        AddChild(AmountEdit);
        AddChild(BuyButton);
        AddChild(SellButton);
    }

    public void Update(StockDisplayData data)
    {
        NameLabel.Text = data.Name;
        CategoryLabel.Text = data.Category;
        PriceLabel.Text = $"${data.Price:F0}";
        OwnedLabel.Text = data.OwnedShares.ToString();

        var change = data.Price - data.PreviousPrice;
        var changePercent = data.PreviousPrice > 0
            ? (change / data.PreviousPrice) * 100f
            : 0f;

        if (change > 0)
        {
            ChangeLabel.Text = $"+{changePercent:F1}%";
            ChangeLabel.FontColorOverride = Color.FromHex("#00CC00");
        }
        else if (change < 0)
        {
            ChangeLabel.Text = $"{changePercent:F1}%";
            ChangeLabel.FontColorOverride = Color.FromHex("#CC0000");
        }
        else
        {
            ChangeLabel.Text = "0.0%";
            ChangeLabel.FontColorOverride = Color.FromHex("#888888");
        }
    }
}
