using Content.Client.UserInterface.Controls;
using Content.Goobstation.Shared.Phones.Components;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;
using Robust.Client.UserInterface.Controls;
using Robust.Shared.Map;

namespace Content.Goobstation.Client.Phones.UI;

[GenerateTypedNameReferences]
public sealed partial class PhoneMenu : FancyWindow
{
    [Dependency] private readonly IEntityManager _entitySystem = default!;
    public event Action<int>? OnKeypadButtonPressed;
    public event Action<int>? OnPhoneBookButtonPressed;
    public event Action? OnClearButtonPressed;
    public event Action? OnEnterButtonPressed;

    public PhoneMenu()
    {
        IoCManager.InjectDependencies(this);
        RobustXamlLoader.Load(this);
        FillKeypadGrid();
    }

    private void FillKeypadGrid()
    {
        // add 3 rows of keypad buttons (1-9)
        for (var i = 1; i <= 9; i++)
        {
            AddKeypadButton(i);
        }

        // clear button
        var clearBtn = new Button()
        {
            Text = "C"
        };
        clearBtn.OnPressed += _ => OnClearButtonPressed?.Invoke();
        KeypadGrid.AddChild(clearBtn);

        // zero button
        AddKeypadButton(0);

        // enter button
        var enterBtn = new Button()
        {
            Text = "D"
        };
        enterBtn.OnPressed += _ => OnEnterButtonPressed?.Invoke();
        KeypadGrid.AddChild(enterBtn);
    }

    private void AddKeypadButton(int i)
    {
        var btn = new Button()
        {
            Text = i.ToString()
        };

        btn.OnPressed += _ => OnKeypadButtonPressed?.Invoke(i);
        KeypadGrid.AddChild(btn);
    }

    public void ClearPhoneBook()
    {
        CommandHeading.DisposeAllChildren();
        SecurityHeading.DisposeAllChildren();
        EngineeringHeading.DisposeAllChildren();
        CargoHeading.DisposeAllChildren();
        MedicalHeading.DisposeAllChildren();
        ScienceHeading.DisposeAllChildren();
        ServiceHeading.DisposeAllChildren();
        UncategorizedHeading.DisposeAllChildren();
    }

    public void AddPhoneBookLabel(string name, string catagory, int phonenumber)
    {
        var btn = new Button()
        {
            Text = name + " - " + phonenumber
        };

        btn.OnPressed += _ => OnPhoneBookButtonPressed?.Invoke(phonenumber);

        switch (catagory)
        {
            case "Command":
                CommandHeading.AddChild(btn);
                break;
            case "Security":
                SecurityHeading.AddChild(btn);
                break;
            case "Engineering":
                EngineeringHeading.AddChild(btn);
                break;
            case "Cargo" or "Supply":
                CargoHeading.AddChild(btn);
                break;
            case "Medical":
                MedicalHeading.AddChild(btn);
                break;
            case "Science":
                ScienceHeading.AddChild(btn);
                break;
            case "Service":
                ServiceHeading.AddChild(btn);
                break;
            default:
                UncategorizedHeading.AddChild(btn);
                break;
        }
    }
}
