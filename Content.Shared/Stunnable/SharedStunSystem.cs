    private void OnStunEndAttempt(Entity<StunnedStatusEffectComponent> entity, ref StatusEffectRelayedEvent<StunEndAttemptEvent> args)
    {

        //todo marty check this shit what the fuck
        if (args.Args.Cancelled)
        // goob edit - stunmeta
